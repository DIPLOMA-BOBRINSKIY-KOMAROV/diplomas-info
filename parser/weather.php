document.addEventListener('DOMContentLoaded', function () {
    setupLiveSearch({
        formId: 'searchFormDesktop',
        inputId: 'cityInputDesktop',
        suggestionsId: 'suggestionsDesktop'
    });

    setupLiveSearch({
        formId: 'searchFormMobile',
        inputId: 'cityInputMobile',
